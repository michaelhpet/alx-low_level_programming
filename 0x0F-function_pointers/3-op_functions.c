/**
 * op_add - sums two integers
 * @a: first integer
 * @b: second integer
 * Return: a + b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the difference of two integers
 * @a: first number
 * @b: second number
 * Return: a - b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the product of two integers
 * @a: first number
 * @b: second number
 * Return: a * b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the result of the division of an integer by another
 * @a: numerator
 * @b: denominator
 * Return: a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of the division of an integer by another
 * @a: dividend
 * @b: divisor
 * Return: a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
